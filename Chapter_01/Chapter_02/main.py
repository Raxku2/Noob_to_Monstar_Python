# Variables & Data Types Example

# Integer
x = 10
print("x:", x, "| Type:", type(x))

# Float
pi = 3.14
print("pi:", pi, "| Type:", type(pi))

# String
name = "Rahul"
print("name:", name, "| Type:", type(name))

# Boolean
flag = True
print("flag:", flag, "| Type:", type(flag))

# List
nums = [1, 2, 3]
print("nums:", nums, "| Type:", type(nums))
